// 5. Вычислить периметр и площадь прямоугольного треугольника по длинам двух его катетов.
class Program
{
    static void Main()
    {
        // Объявляем переменные для хранения длин катетов
        double a, b;

        // Вводим длины катетов с клавиатуры и преобразуем их в вещественные числа (double)
        Console.Write("Введите длину первого катета: ");
        a = Convert.ToDouble(Console.ReadLine());

        Console.Write("Введите длину второго катета: ");
        b = Convert.ToDouble(Console.ReadLine());

        // Вычисляем длину гипотенузы с использованием теоремы Пифагора
        double c = Math.Sqrt(a * a + b * b);

        // Вычисляем периметр треугольника (сумма всех сторон)
        double perimeter = a + b + c;

        // Вычисляем площадь треугольника (по формуле для прямоугольного треугольника)
        double area = 0.5 * a * b;

        // Выводим результаты на экран
        Console.WriteLine("Периметр треугольника: " + perimeter);
        Console.WriteLine("Площадь треугольника: " + area);

        // Ожидаем, пока пользователь нажмет Enter, чтобы закрыть консоль
        Console.WriteLine("Нажмите Enter для выхода...");
        Console.ReadLine();
    }
}
